#!/usr/bin/env python3

from bs4 import BeautifulSoup
import json
import sys


def cat_patterns(p1, p2):
    if p1["len"] != p2["len"]:
        raise RuntimeError(
            f"Patterns have distinct lenghts: {p1['len']} != {p2['len']}"
        )

    cat_vals = []
    generated_p1_bytes = generate_bytes(p1)
    generated_p2_bytes = generate_bytes(p2)
    for i in range(max(len(generated_p1_bytes), len(generated_p2_bytes))):
        i1 = min(len(generated_p1_bytes) - 1, i)
        i2 = min(len(generated_p2_bytes) - 1, i)
        if p1["re"] or p2["le"]:
            if p1["re"]:
                mask = p2["mask"]
            if p2["le"]:
                mask = p1["mask"]
            p1_masked_bytes = generated_p1_bytes[i1] & mask
            p2_masked_bytes = generated_p2_bytes[i2] & mask
            if p1_masked_bytes != p2_masked_bytes:
                continue

        cat_vals.append(generated_p1_bytes[i1] | generated_p2_bytes[i2])

    if len(cat_vals) == 0:
        return None

    return {
        "len": p2["len"],
        "kind": 0,
        "le": False,
        "re": p2["re"],
        "vals": cat_vals,
    }


def generate_bytes(pattern):
    expanded_vals = []
    if pattern["kind"] == 0:
        vals = pattern["vals"]
    elif pattern["kind"] == 1:
        vals = []
        # To optimize performance, we only generate the first value
        # of patterns with multiple bytes.
        if pattern["len"] == 8:
            for val in range(pattern["vals"][0], pattern["vals"][1] + 1):
                vals.append(val)
        else:
            vals.append(pattern["vals"][0])
    else:
        raise RuntimeError(f"Unhandled kind: {pattern['kind']}.")

    for val in vals:
        for i in range(pattern["len"] // 8):
            # TODO: Consider endianness.
            expanded_vals.append((val >> (i * 8)) & 0xFF)

    return expanded_vals


def generate_pattern_bytes(patterns, len_patterns, i, acc, acc2):
    # print(patterns, len_patterns, i, acc, acc2)
    len_patterns_sub = len_patterns
    while i < len(patterns):
        pattern = patterns[i]
        if pattern["le"] == True and pattern["re"] == True:
            raise RuntimeError(f"Pattern {pattern} has double ellipsis.")

        if i < len(patterns) - 1:
            next_pattern = patterns[i + 1]
            if pattern["re"] == True and next_pattern["le"] == True:
                raise RuntimeError(
                    f"Patterns {pattern} and {next_pattern} intersect on ellipsis."
                )

            if pattern["re"] == True or next_pattern["le"] == True:
                i += 1
                pattern = cat_patterns(pattern, next_pattern)
                if not pattern:
                    # Masked pattern values did not match, this isn't a valid sequence.
                    acc2.clear()
                    return

                # Since we concatenated two patterns, we expect one less byte sequence.
                len_patterns_sub -= 1
        i += 1

        generated_i = 0
        generated_bytes = generate_bytes(pattern)
        while generated_i < len(generated_bytes):
            acc_sub = acc[:]

            # Update expected final length of byte sequence according to generated bytes.
            len_patterns_sub -= 1
            for _ in range(pattern["len"] // 8):
                acc_sub.append(generated_bytes[generated_i])
                generated_i += 1
                len_patterns_sub += 1

            generate_pattern_bytes(patterns, len_patterns_sub, i, acc_sub, acc2)
            if i == len(patterns) and len(acc_sub) == len_patterns_sub:
                acc2.append(acc_sub)


def generate_sequences(instructions):
    sequences = {}
    for instruction in instructions:
        instruction_name = instruction[0]
        for instruction_pattern in instruction[1]:
            pattern_bytes = []
            generate_pattern_bytes(
                instruction_pattern, len(instruction_pattern), 0, [], pattern_bytes
            )
            if len(pattern_bytes) == 0:
                continue

            sequence_len = len(pattern_bytes[0])
            if sequence_len not in sequences:
                sequences[sequence_len] = []
            sequences[sequence_len].append(pattern_bytes)
            # print(instruction_name, pattern_bytes)
    # print(sequences)

    return sequences


def render(sequences):
    template = """
<!doctype html>
<html lang="en">
    <head>
        <meta charset="utf-8">
        <meta name="viewport" content="width=device-width, initial-scale=1">
        <title>ISAcrumbs</title>
    </head>
    <style>
    body {
        background-color: #cdf;
        font-family: monospace;
    }
    table {
        display: inline-block;
        border: 1px solid #abe;
        padding: 0.5rem;
    }
    </style>
    <body>
    </body>
</html>
"""

    soup = BeautifulSoup(
        template,
        features="html.parser",
    )

    for i in sorted(sequences.keys()):
        for ii in range(i):
            filled_bytes = [False] * 256
            for j in range(len(sequences[i])):
                for k in range(len(sequences[i][j])):
                    # TODO: Check beyond first byte.
                    pattern_bytes = sequences[i][j][k]
                    filled_bytes[pattern_bytes[ii]] = True

            table = soup.new_tag("table")

            tr = soup.new_tag("tr")
            th = soup.new_tag("th", string="Hi\\Lo")
            tr.append(th)
            for label_i in range(16):
                th = soup.new_tag("th", string=f"{label_i:01x}")
                tr.append(th)
            table.append(tr)

            for label_i in range(16):
                tr = soup.new_tag("tr")
                td = soup.new_tag(
                    "td",
                    attrs={"style": "font-weight: bold; text-align: right"},
                    string=f"{label_i:01x}",
                )
                tr.append(td)
                for j in range(16):
                    if filled_bytes[(label_i << 4) | j]:
                        td = soup.new_tag(
                            "td", attrs={"style": "background-color: #698"}
                        )
                    else:
                        td = soup.new_tag("td")
                    tr.append(td)
                table.append(tr)

            soup.html.body.append(table)
        soup.html.body.append(soup.new_tag("br"))

        with open("/tmp/out.html", "w") as f:
            f.write(soup.prettify(formatter=None))

    with open("/tmp/out.hex", "wb") as f:
        for sequence_by_instr_len in sequences.values():
            for instr_sequence in sequence_by_instr_len:
                for sequence in instr_sequence:
                    for byte in sequence:
                        f.write(bytes([byte]))


with open(sys.argv[1], "r") as f:
    instructions = json.load(f)
    sequences = generate_sequences(instructions)
    render(sequences)
